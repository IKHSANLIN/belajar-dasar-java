public class Variable {
    public static void main(String[] args) {
        String name = "Ikhsan";
        int age = 21;
        String address = "Cirebon";
        System.out.println("Hello, my name is " + name + ", I am " + age + " years old, and I live in " + address + ".");
        var name2 = "Ikhsan";
        var age2 = 24;
        var address2 = "Cirebon";
        System.out.println("Hello, my name is " + name2 + ", I am " + age2 + " years old, and I live in " + address2 + ".");
    }
}
